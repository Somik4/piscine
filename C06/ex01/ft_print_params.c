#include <unistd.h>

int	main(int arc, char **arv)
{
	int	i;
	int	j;

	j = 1;
	while (arc > j)
	{
		i = 0;
		while (arv[j][i] != '\0')
		{
			write(1, &arv[j][i], 1);
			i++;
		}
		write (1, "\n", 1);
		j++;
	}
	return (0);
}
