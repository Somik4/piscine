#include<unistd.h>

int	main(int arc, char **arv)
{
	int	a;
	int	i;

	a = arc - 1;
	while (a > 0)
	{
		i = 0;
		while (arv[a][i])
		{
			write (1, &arv[a][i], 1);
			i++;
		}
		a--;
		write(1, "\n", 1);
	}
	return (0);
}
