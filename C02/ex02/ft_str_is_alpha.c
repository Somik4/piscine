int	ft_str_is_alpha(char *str)
{
	int	a;

	a = 0;
	if (str [a] == '\0')
	{
		return (1);
	}
	while (str[a] != '\0')
	{
		if ((str[a] >= 'A' && str[a] <= 'Z')
			|| (str[a] >= 'a' && str[a] <= 'z'))
		{
			a++;
		}
		else
			return (0);
	}
	return (1);
}
