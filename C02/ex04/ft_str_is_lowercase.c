int	ft_str_is_lowercase(char *str)
{
	int	a;

	a = 0;
	if (str [a] == '\0')
		return (1);
	while (str [a] != '\0')
	{
		if (str [a] >= 'a' && str [a] <= 'z' )
			return (1);
		a++;
	}
	return (0);
}
