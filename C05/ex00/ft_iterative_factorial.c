int	ft_iterative_factorial(int nb)
{
	int	i;
	int	num;

	i = 1;
	num = 1;
	if (nb < 0 || nb > 2147483647)
		return (0);
	else if (nb == 0)
		return (1);
	while (nb > 1)
	{
		num = num * nb;
		nb --;
	}
	return (num);
}
